const horPages = ["start", "page2", "page3", "small-page"];
const toDoList = ["bouwmaterialen", "groendak", "ventileren"];
const didThis = [];
var crrntPg;


//makes it so that we scroll when we wanna inside subject
$(document).on("click", "#bttnL", function(){
    console.log($(`#${correctScene}`).find(`.${horPages[crrntPg]}`).offset().left);
    if(crrntPg == 0){
        stopVideo();
    }
    if(crrntPg > 0){
        crrntPg--;
        var nextScL = $(`#${correctScene}`).find(`.${horPages[crrntPg]}`).offset().left;
        $("html, body").animate({ scrollLeft: `${nextScL}px` });
    } else {
        returnToHouse();
    }
    changeBttnTxt();
});

$(document).on("click", "#bttnR", function(){
    if(crrntPg == 0){
        stopVideo();
    }
    if(crrntPg < (horPages.length - 1)){
        crrntPg++;
        var nextScL;
        $(`.${horPages[crrntPg]}`).each(function(){
            var tempScl = $(`#${correctScene}`).find(`.${horPages[crrntPg]}`).offset().left;

            if(tempScl > 0){
                nextScL = tempScl;
            }
        });
        $("html, body").animate({ scrollLeft: `${nextScL}px` });
    } else {
        returnToHouse();
    }
    changeBttnTxt();
});

function changeBttnTxt(){
    var LeftH3 = $("#bttnL").find("h3");
    var RightH3 = $("#bttnR").find("h3");
    if (crrntPg == 3){
        RightH3.html("Naar Huis");
    } else{
        RightH3.html("Meer Weten");
    }
    if (crrntPg == 0){
        LeftH3.html("Naar Huis");
    } else{
        LeftH3.html("&lt;--");
    }
}

function returnToHouse(){
    if(isInToDoList() == false){
        didThis.push(sceneBtnnId);
    }
    
    console.log(didThis);
    if(didThis.length >= toDoList.length){
        $("#finale").removeClass("hidden");
        $("#scene7").removeClass("hidden");
        $("#scene8").removeClass("hidden");
        
        if(DoneItAll == false){
            DoneItAll = true;
        }
    }
    if(DoneItAll == true){
        $("#scene7").removeClass("hidden");
        $("#scene8").removeClass("hidden");
    }

    $("#scene0").removeClass("hidden");
    $("#scene1").removeClass("hidden");
    $("#scene2").removeClass("hidden");
    $("#scene3").removeClass("hidden");

    $(`#${sceneBtnnId}`).addClass("hoofdstukgedaan");
    $("#controls").addClass("hidden");
    $(`#${correctScene}`).addClass("hidden");
    var sceneTop = $(`#scene3`).offset().top - 50;
    $("html, body").animate({ scrollTop: `${sceneTop}px` });
    $("body").removeClass("lock");
}

function stopVideo(){
    var crrntvid = sceneBtnnId + "vid";
    var vid = document.getElementById(crrntvid);
    vid.pause();
}

function isInToDoList(){
    for (let i = 0; i < didThis.length; i++) {
        const element = didThis[i];
        if(sceneBtnnId == element){
            return true;
        }
    }
    return false;
}

